const latestConfigVersion = {
  8453: '2.2.0',
};

function resolveConfigVersion(chainId, contractVersion) {
  const versions = Object.keys(chainConfigsByVersion[chainId] || {}).sort(compareVersion);
  if (versions.length === 0) {
    throw new Error(`Unsupported chain ID: ${chainId}`);
  }
  if (!contractVersion) {
    return latestConfigVersion[chainId] || versions[versions.length - 1];
  }
  if (chainConfigsByVersion[chainId]?.[contractVersion]) {
    return contractVersion;
  }

  const compatible = versions.filter((version) => compareVersion(version, contractVersion) <= 0);
  if (compatible.length > 0) {
    return compatible[compatible.length - 1];
  }

  return versions[0];
}

