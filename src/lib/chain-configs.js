export function getChainConfig(chainId, contractVersion) {
  const version = resolveConfigVersion(chainId, contractVersion);
  const config = chainConfigsByVersion[chainId]?.[version];
  if (!config) {
    throw new Error(`Unsupported chain ID: ${chainId}`);
  }
  return config;
}

export function normalizePair(pair = 'ETH') {
  return String(pair).trim().toUpperCase();
}

export function getCollateralTemplate(chainId, pair = 'ETH', contractVersion) {
  const config = getChainConfig(chainId, contractVersion);
  const normalizedPair = normalizePair(pair);
  const template = config.COLLATERAL_TEMPLATES?.[normalizedPair];
  if (template) {
    return template;
  }
  if (normalizedPair === 'ETH') {
    return buildLegacyEthTemplate(config);
  }
  throw new Error(`Unsupported pair: ${pair}`);
}

export function getCollateralTemplateByAddress(chainId, collateralAddress, contractVersion) {
  const config = getChainConfig(chainId, contractVersion);
  const normalized = String(collateralAddress || ZERO_ADDRESS).toLowerCase();

  if (isNativeCollateralAddress(collateralAddress, config)) {
    return config.COLLATERAL_TEMPLATES?.ETH || buildLegacyEthTemplate(config);
  }

  for (const template of Object.values(config.COLLATERAL_TEMPLATES || {})) {
    if (String(template.COLLATERAL_TOKEN).toLowerCase() === normalized) {
      return template;
    }
  }

  return null;
}

export function isNativeCollateralAddress(address, chainConfig) {
  if (!address) return true;
  const value = String(address).toLowerCase();
  return value === ZERO_ADDRESS.toLowerCase() || value === String(chainConfig.WETH).toLowerCase();
}

